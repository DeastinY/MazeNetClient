
namespace MazeNetClient.AI
{
    /// <summary>
    /// Describes a clockwise rotation.
    /// </summary>
    enum Rotation
    {
        /// <summary>
        /// A rotation of 0°.
        /// </summary>
        DEGREE_0,

        /// <summary>
        /// A rotation of 90°.
        /// </summary>
        DEGREE_90,

        /// <summary>
        /// A rotation of 180°.
        /// </summary>
        DEGREE_180,

        /// <summary>
        /// A rotation of 270°.
        /// </summary>
        DEGREE_270
    }
}